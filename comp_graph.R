x <- seq(0, 3, 0.001)

h <- function(x) {
	return(2^10/(x^10) * (1 + 0.85/x)^-3 * (1 + 1.08/x)^-3
 * (1 + 0.35/x)^-3
 * (1 + 3.28/x)^-3
 * (1 + 1.24/x)^-3
 * (1 + 2.58/x)^-3
 * (1 + 0.02/x)^-3
 * (1 + 0.13/x)^-3
 * (1 + 0.22/x)^-3
 * (1 + 0.52/x)^-3)
}

y = h(x)
plot(x, y, type='l', xlab='theta', ylab='likelihood')
